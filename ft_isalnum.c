int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
/*ft_isalnum return 1 if true (if character a - z , A - Z & digitnumber 0 - 9)
return 0 if false */
